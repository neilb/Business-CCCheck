# expand ranges in hash
#
# input:	pointer to hash
# returns:	nothing
#
# expands keys of the form: 123-125 to 123, 124, 125 pointing to the same value


sub _re_range {
  my $hp = $_[0];
  my @ranges = grep { /\-/ } keys %$hp;
  foreach (@ranges) {
    my $val = delete $hp->{$_};
    my($n,$end) = split('-',$_);
    for (; $n <= $end; $n++) {
      $hp->{$n} = $val
	unless exists $hp->{$n};
    }
  }
}

# expand ranges in CCsecondary
#
_re_range(\%CCsecondary);

1;

